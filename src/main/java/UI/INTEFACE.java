    void ADD_TABLE(){
        DefaultTableModel Table = (DefaultTableModel)jTable1.getModel();
        Table.setRowCount(0);
        for(int i = 0; i < User.size(); i++){
            Table.addRow(new Object[6]);
            jTable1.setValueAt(User.get(i).getID(), i, 0);
            jTable1.setValueAt(User.get(i).getFirstName(), i, 1);
            jTable1.setValueAt(User.get(i).getLastName(), i, 2);
            jTable1.setValueAt(User.get(i).getNameCourse(), i, 3);
            jTable1.setValueAt(User.get(i).getNote_P1(), i, 4);
            jTable1.setValueAt(User.get(i).getNote_P2(), i, 5);
            jTable1.setValueAt(User.get(i).getNote_P3(), i, 6);
            //=============================
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jComboBox1.setSelectedIndex(0);
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
        }
    }
    void DATA(){
        ESTUDIANTE ES = new ESTUDIANTE(Double.parseDouble(jTextField4.getText()),
                Double.parseDouble(jTextField5.getText()),
                Double.parseDouble(jTextField6.getText()),
                jComboBox1.getSelectedItem().toString(),
                jTextField1.getText(),
                jTextField2.getText(),
                jTextField3.getText());
        User.add(ES);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jComboBox1.setSelectedIndex(0);
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
    }    
